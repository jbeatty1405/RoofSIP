export default function MobileNav({ unread }: { unread: number }) {
  const pathname = usePathname()

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 bg-white border-t border-zinc-200 flex items-center z-50">
      {NAV.map(({ href, label, icon }) => {
        const active = pathname === href || (href !== '/dashboard' && pathname.startsWith(href))
        const isAlerts = href === '/notifications'
        return (
          <Link
            key={href}
            href={href}
            className={`flex-1 flex flex-col items-center gap-1 py-3 relative transition-colors ${
              active ? 'text-sky-500' : 'text-zinc-400'
            }`}
          >
            {icon}
            <span className="text-[10px] font-medium">{label}</span>
            {isAlerts && unread > 0 && (
              <span className="absolute top-2 right-1/2 translate-x-3 w-4 h-4 bg-red-500 text-white text-[9px] font-bold rounded-full flex items-center justify-center">
                {unread > 9 ? '9+' : unread}
              </span>
            )}
          </Link>
        )
      })}
    </nav>
  )
}
